import SwiftUI

extension Font {
    public static func golosUI(size: CGFloat) -> Font {
        return Font.custom("GolosUI-VF", size: size)
    }

    public static func literata(size: CGFloat) -> Font {
        return Font.custom("LiterataVariable", size: size)
    }
}
